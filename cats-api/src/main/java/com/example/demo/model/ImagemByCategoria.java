package com.example.demo.model;

public class ImagemByCategoria {
	
	//ATRIBUTOS
	private Raca breeds;
	private Categoria categories;
	private String id;
	private String url;
	private int width;
	private int height;
	
	//GETTERS E SETTERS
	public Raca getBreeds() {
		return breeds;
	}
	public void setBreeds(Raca breeds) {
		this.breeds = breeds;
	}
	public Categoria getCategories() {
		return categories;
	}
	public void setCategories(Categoria categories) {
		this.categories = categories;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	
	

}
